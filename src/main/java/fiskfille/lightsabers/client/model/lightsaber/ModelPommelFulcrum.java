        GL11.glTranslatef(-this.bottom1.offsetX, -this.bottom1.offsetY, -this.bottom1.offsetZ);
        GL11.glTranslatef(-this.bottom1.rotationPointX * f5, -this.bottom1.rotationPointY * f5, -this.bottom1.rotationPointZ * f5);
        this.bottom1.render(f5);
        GL11.glPopMatrix();
        this.body1.render(f5);
        GL11.glPushMatrix();
        GL11.glTranslatef(this.ring1.offsetX, this.ring1.offsetY, this.ring1.offsetZ);
        GL11.glTranslatef(this.ring1.rotationPointX * f5, this.ring1.rotationPointY * f5, this.ring1.rotationPointZ * f5);
